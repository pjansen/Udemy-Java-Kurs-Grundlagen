
public class IntFloatDouble {

	public static void main(String[] args) {

		// Flie√ükommazahlen werden automatisch als double gesehen
		// mit ...f wird float verwendet
		
		int varZahl1 = 10 / 3;
		float varZahl2 = 10f / 3; // ...f
		double varZahl3 = 10d / 3; // ...d
		
		System.out.println("int " + varZahl1);
		System.out.println("float " + varZahl2);
		System.out.println("double " + varZahl3);
		
	}

}